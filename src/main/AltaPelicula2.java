    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String titulo = Titulo.getText();
        String titulo2 = Titulo2.getText();
        int a単o = Integer.parseInt(A単o.getText());
        int estatus;
        if(NDisponible.isSelected()==true){
            estatus = 1;
        }else if(Disponible.isSelected()==true){
            estatus = 2;
        } else{
            estatus = 1;
        }
        String pais = Paises.getSelectedItem().toString();
        String director= Direccion.getSelectedItem().toString();
        try{
            Connection con = Conexion.getConexion();
            PreparedStatement ps = con.prepareStatement("exec SP_AltaPelicula @Titulo=?,@Titulo2=?,@A単o=?,@Estatus=?,@Pais=?,@Director=?");
            ps.setString(1, titulo);
            ps.setString(2, titulo2);
            ps.setInt(3, a単o);
            ps.setInt(4, estatus);
            ps.setString(5, pais);
            ps.setString(6, director);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Registro Guardado");
            limpiar();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

